let members = ["Aarya", "Aasha", "Anisha", "Amit", "Anjana", "Bijay", "Bikash"]


/* 
    Index in array
    the index of an array always starts with 0 

    to access the value or to change the value we have to use index

*/

console.log(members[0]);  // Aarya
console.log(members[1]);  // Asha
console.log(members[6]);  // Bikash


members[6] =  "Bishal"  // we are changing the  6th index value of members array 

console.log(members[6]); //Bishal